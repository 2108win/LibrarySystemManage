    private int xMouse;
    private int yMouse;

    public void initMoving(JFrame fram) {
        this.addMouseListener(new MouseAdapter() {
            @Override
            public void mousePressed(MouseEvent me) {
                xMouse = me.getX();
                yMouse = me.getY();
            }
        });
        this.addMouseMotionListener(new MouseMotionAdapter() {
            @Override
            public void mouseDragged(MouseEvent me) {
                fram.setLocation(me.getXOnScreen() - xMouse, me.getYOnScreen() - yMouse);
            }
        });
    }

    public Boolean validateLoginDetails() {
        boolean isValidate;
        String name = txt_username.getText();
        String password = String.valueOf(txt_password.getPassword());

        if (name.isEmpty()) {

            checkErrorLabel.setText("*Please fill username");
            isValidate = false;
        } else if (password.isEmpty()) {
            checkErrorLabel.setText("*Please fill password");
            isValidate = false;
        } else {
            checkErrorLabel.setText(" ");
            isValidate = true;
        }

        return isValidate;
    }

    public void login() {
        String name = txt_username.getText();
        String password = String.valueOf(txt_password.getPassword());
        UsersDao ud = new UsersDao();
        Users user = ud.userLogin(name, password);
        if (user == null) {
            checkErrorLabel.setText("*Invalid username or password");
        } else {
            LoginPage.currentUser = user;
            JOptionPane.showMessageDialog(LoginPage.this, "Login Successful");
            HomePage home = new HomePage();
            home.setVisible(true);
            this.dispose();
        }
    }

